
public class WhileLoop {

	public static void main(String[] args) {
		
		int i = 11;
		
		while(i <= 10) {
			System.out.println(i);
		}


	
	
	int j = 11;
	do {
		System.out.println(j);
		j++;
	}while(j <= 10);

	
	int x = 1, y=1;
	do {
		x++;
		y++;
		System.out.println(x +" "+ y);
	}while(x < 10 || y < 10);
}}
